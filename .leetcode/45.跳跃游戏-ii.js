var jump = function (nums) {
  const map = {}
  const target = nums.length - 1
  const check = (i) => {
    if (map[i] !== undefined) return map[i]
    if (i === target) {
      map[i] = 0
      return map[i]
    }
    const v = nums[i]
    if (v === 0) {
      map[i] = false
      return map[i]
    }
    if (target - i <= v) {
      map[i] = 1
      return map[i]
    }
    let minStep
    for (let j = v; j >= 1; j--) {
      const nextStep = check(i + j)
      if (nextStep === false) continue
      const step = 1 + nextStep
      minStep = minStep ? Math.min(minStep, step) : step
    }
    map[i] = minStep || false
    return map[i]
  }
  for (let i = target; i >= 0; i--) {
    check(i)
  }
  console.log('map', map)
  return map[0] || 0
}
